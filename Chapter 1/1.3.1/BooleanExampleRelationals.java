public class BooleanExampleRelationals 
{
  public static void main(String[] args) 
  {
    int a = 5;
    int b = 8;
    int c = 2;
    int d = 5;
    
    System.out.println("a <= 5: " + (a <= 5));
    System.out.println("a == d: " + (a == d));
    System.out.println("a > c: " + (a > c));
    System.out.println("a >= 5: " + (a >= 5));
    System.out.println("a != b: " + (a != b));
    System.out.println("a < b: " + (a < b));
  }
}