public class EnhancedFor {
    public static void main(String[] args) {
        int[] goals = {1, 2, 0, 3, 2, 4, 2, 1, 0, 2, 0, 1, 3, 2};
        // Loops through the entire array (goals)
        for (int g : goals) {
            System.out.println("goals:" + g);
        }
    }
}
